-----PROCEDIMIENTO dias trabajados

CREATE PROCEDURE reporte_horas 
	@fecha Date,
	@id int
AS
BEGIN
	SELECT HORA_ENTRADA, HORA_SALIDA, H.FECHA, NOMBRE, C.NOMBRE_CARGO,
	HORAS=CONCAT(datediff( HOUR,HORA_ENTRADA,HORA_SALIDA),' Horas')
	FROM HORARIO H
	inner join PERSONA P ON (H.ID_PERSONA = P.ID_PERSONA)
	INNER JOIN CARGO C ON(P.ID_CARGO = C.ID_CARGO)
	WHERE DATEPART(MONTH,H.FECHA) = DATEPART(MONTH,@fecha)
	AND H.ID_PERSONA=@id;
END







-------------------- PROCEDIMIENTO ALMACENADO DEL SUELDO CON LOS DESCUENTOS ASIGNADOS------------
CREATE PROCEDURE SUELDO_CANCELAR
	@fecha date,
	@id int
AS
BEGIN
	SELECT P.NOMBRE,P.APE_MATERNO,P.APE_MATERNO,C.NOMBRE_CARGO,C.SUELDO_FIJO,
	SUM (datediff( HOUR,HORA_ENTRADA,HORA_SALIDA)) AS TOTAL_HORAS,
	COUNT(H.HORA_ENTRADA) DIAS_TRABAJADOS,
	UPPER( datename(MONTH,H.FECHA)) AS MES_TRABAJADO,
	SUELDO=((SUELDO_FIJO/28)/8)*SUM (datediff( HOUR,HORA_ENTRADA,HORA_SALIDA)),
	DESCUENTO_CAJA_SALUD=(((SUELDO_FIJO/28)/8)*SUM (datediff( HOUR,HORA_ENTRADA,HORA_SALIDA)))*0.10,
	DESCUENTO_AFPs=(((SUELDO_FIJO/28)/8)*SUM (datediff( HOUR,HORA_ENTRADA,HORA_SALIDA)))*0.12,
	TOTAL_CANCELAR=((SUELDO_FIJO/28)/8)*SUM (datediff( HOUR,HORA_ENTRADA,HORA_SALIDA))-((((SUELDO_FIJO/28)/8)*SUM (datediff( HOUR,HORA_ENTRADA,HORA_SALIDA)))*0.10)-((((SUELDO_FIJO/28)/8)*SUM (datediff( HOUR,HORA_ENTRADA,HORA_SALIDA)))*0.12)
	FROM HORARIO H
	INNER JOIN PERSONA P
	ON (H.ID_PERSONA=P.ID_PERSONA)
	INNER JOIN CARGO C 
	ON (P.ID_CARGO =C.ID_CARGO)
	WHERE H.ID_PERSONA=@id and DATEPART(MONTH,H.FECHA) = DATEPART(MONTH,@fecha)
	GROUP BY NOMBRE,APE_MATERNO,APE_MATERNO,C.NOMBRE_CARGO,C.SUELDO_FIJO,datename(MONTH,H.FECHA);

END
